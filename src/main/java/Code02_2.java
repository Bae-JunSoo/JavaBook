public class Code02_2 {

    public static void main(String[] args) {
        int a, b, c;

        a = 100;
        b = 50;

        c = a + b;
        System.out.println(a + "+" + b + "=" + c);

        c = a - b;
        System.out.println(a + "-" + b + "=" + c);

        c = a * b;
        System.out.println(a + "*" + b + "=" + c);

        c = a / b;
        System.out.println(a + "/" + b + "=" + c);
    }
}
