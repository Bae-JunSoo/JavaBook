public class Code03_8 {
    public static void main(String[] args) {

        int a = 99;

        System.out.println((a > 100) && (a < 200) );    // && = 그리고
        System.out.println((a == 99) || (a == 100) );   // || = 또는
        System.out.println(!(a == 100) );               // ! = 부정
    }
}
